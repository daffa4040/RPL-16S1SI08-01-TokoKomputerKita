<?php
	class produk(){
		private $kodeproduk;
		private $namaproduk;
		private $harga;
		private $stok;
		//private $Image;  
	}

	function setKodeproduk(){
		$this->kodeproduk=$kodeproduk;
	}
	function getKodeproduk(){
		return $this->kodeproduk;
	}

	function setNamaproduk(){
		$this->namaproduk=$namaproduk;
	}
	function getNamaproduk(){
		return $this->namaproduk;
	}

	function setHarga(){
		$this->harga=$harga;
	}
	function getHarga(){
		return $this->harga;
	}

	function setStok(){
		$this->stok=$stok;
	}
	function getStok(){
		return $this->stok;
	}
